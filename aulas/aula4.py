# Tipos int e float

# int -> Número inteiro
# print int = -3, -2, -1, 0, 1, 2, 3
print(10) # int
print(-10) # int
print(0) # int

# float -> Número real
# float -> -1, -0.5, 0, 0.5, 1
print(3.1415) #float
print(-1.333) #float
print(0.0) #float

# tem casa decimal -> float
# Não tem casa deciamla -> int

# A função type mostra o tipo que o Python
# inferiu ao valor
print(type('Válter'))
print(type(10))
print(type(1.5))