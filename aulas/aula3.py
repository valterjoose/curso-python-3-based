'''
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string ->  texto
Strings são textos que estão dentro de aspas
'''

# Aspas simples
print('Válter José')
print(1, 'Válter "José"')

# Aspas duplas
print("Válter José")
print("Válter José")

# Escape
print(2, "Válter 'José'")

# r
print(r"Válter \"José\"")

